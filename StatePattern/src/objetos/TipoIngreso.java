package objetos;

/**
 *
 * @author Diego
 */
public class TipoIngreso {
    
    private String nombre, descripcion;

    public TipoIngreso(String nombre, String descripcion) {
        this.nombre = nombre;
        this.descripcion = descripcion;
    }

    public String getNombre() {
        return nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }
    
    
}
